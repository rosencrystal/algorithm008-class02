# [88] 合并两个有序数组
#

# @lc code=start
class Solution:
    def merge(self, nums1: list, m: int, nums2: list, n: int) -> None:
        """
        Do not return anything, modify nums1 in-place instead.
        """
        for i, num in enumerate(nums2):
            nums1[m + i] = num
        nums1.sort()    
